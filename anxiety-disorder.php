                            <h3>Anxiety Disorder; A Serious Concern</h3>
                            <img src="images/anxiety-disorders.jpg" class="img-responsive" alt='' />
                            <h4>What Are Anxiety Disorders?</h4>
                             <p>Anxiety is a normal reaction to stress and can be beneficial in some situations. It can alert us to dangers and help us prepare and pay attention. Anxiety disorders differ from normal feelings of nervousness or anxiousness, and involve excessive fear or anxiety. Anxiety disorders are the most common of mental disorders and affect nearly 30 percent of adults at some point in their lives. But anxiety disorders are treatable and a number of effective treatments are available.</p>
                             <p>Treatment helps most people lead normal productive live. Anxiety refers to anticipation of a future concern and is more associated with muscle tension and avoidance behaviour. Fear is an emotional response to an immediate threat and is more associated with a fight or flight reaction – either staying to fight or leaving to escape danger. Anxiety disorders can cause people into try to avoid situations that trigger or worsen their symptoms. Job performance, school work and personal relationships can be affected. In general, for a person to be diagnosed with an anxiety disorder, the fear or anxiety must: Be out of proportion to the situation or age inappropriate, hinder your ability to function normally.</p>
<h4>Risk Factors</h4>
<p>The causes of anxiety disorders are currently unknown but likely involve a combination of factors including genetic, environmental, psychological and developmental. Anxiety disorders can run in families, suggesting that a combination of genes and environmental stresses can produce the disorders.</p>
<h4>Diagnosis and Treatment</h4>
<p>The first step is to see your doctor to make sure there is no physical problem causing the symptoms. If an anxiety disorder is diagnosed, a mental health professional can work with you on the best treatment. Unfortunately, many people with anxiety disorders don’t seek help. They don’t realize that they have an illness that has effective treatments.</p>
<p>Although each type of anxiety disorder has unique characteristics, most respond well to two types of treatment: <em><strong>psychotherapy, or “talk therapy,” and medications</strong></em>. These treatments can be given alone or in combination. Cognitive behavior therapy (CBT), a type of talk therapy, can help a person learn a different way of thinking, reacting and behaving to help feel less anxious. Medications will not cure anxiety disorders, but can give significant relief from symptoms. The most commonly used medications are anti-anxiety medications (generally prescribed only for a short period of time) and antidepressants. Beta-blockers, used for heart conditions, are sometimes used to control physical symptoms of anxiety.</p>
<h4>Self-Help, Coping, and Managing</h4>
<p>There are a number of things people do to help cope with symptoms of anxiety disorders and make treatment more effective. Stress management techniques and meditation can be helpful. Support groups (in-person or online) can provide an opportunity to share experiences and coping strategies. Learning more about the specifics of a disorder and helping family and friends to understand better can also be helpful. Avoid caffeine, which can worsen symptoms, and check with your doctor about any medications.
</p>
