                          <h3>Alzheimer's Disease</h3>
                            <img src="images/alzheimer-disease.jpg" class="img-responsive" alt='' />
                            <h4>What is Alzheimer's Disease?</h4>
                             <p>Alzheimer's disease (AD) is an age-related, non-reversible brain disorder that develops over a period of years. Initially, people experience memory loss and confusion, which may be mistaken for the kinds of memory changes that are sometimes associated with normal aging. However, the symptoms of AD gradually lead to behavior and personality changes, a decline in cognitive abilities such as decision-making and language skills, and problems recognizing family and friends. <p/>
                             <p>AD ultimately leads to a severe loss of mental function. These losses are related to the worsening breakdown of the connections between certain neurons in the brain and their eventual death. AD is one of a group of disorders called dementias that are characterized by cognitive and behavioral problems. It is the most common cause of dementia among people age 65 and older.</p>
                             <p>There are three major hallmarks in the brain that are associated with the disease processes of AD. Amyloid plaques, which are made up of fragments of a protein called beta-amyloid peptide mixed with a collection of additional proteins, remnants of neurons, and bits and pieces of other nerve cells. Neurofibrillary tangles (NFTs), found inside neurons, are abnormal collections of a protein called tau. Normal tau is required for healthy neurons. However, in AD, tau clumps together. As a result, neurons fail to function normally and eventually die.</p>
                             <p>Loss of connections between neurons responsible for memory and learning. Neurons can't survive when they lose their connections to other neurons. As neurons die throughout the brain, the affected regions begin to atrophy, or shrink. By the final stage of AD, damage is widespread and brain tissue has shrunk significantly.</p>
<h4>Treatment</h4>
<p>Currently there are no medicines that can slow the progression of AD. However, four FDA-approved medications are used to treat AD symptoms. These drugs help individuals carry out the activities of daily living by maintaining thinking, memory, or speaking skills. They can also help with some of the behavioral and personality changes associated with AD. However, they will not stop or reverse AD and appear to help individuals for only a few months to a few years. Donepezil (Aricept), rivastigmine (Exelon), and galantamine (Razadyne) are prescribed to treat mild to moderate AD symptoms. Donepezil was recently approved to treat severe AD as well. The newest AD medication is memantine (Namenda), which is prescribed to treat moderate to severe AD symptoms.
</p>
<h4>Prognosis</h4>
<p>In very few families, people develop AD in their 30s, 40s, and 50s. This is known as "early onset" AD. These individuals have a mutation in one of three different inherited genes that causes the disease to begin at an earlier age. More than 90 percent of AD develops in people older than 65. This form of AD is called "late-onset" AD, and its development and pattern of damage in the brain is similar to that of early-onset AD. The course of this disease varies from person to person, as does the rate of decline. In most people with AD, symptoms first appear after age 65.</p>
<p>We don't yet completely understand the causes of late-onset AD, but they probably include genetic, environmental, and lifestyle factors. Although the risk of developing AD increases with age, AD and dementia symptoms are not a part of normal aging. There are also some forms of dementia that aren't related to brain diseases such as AD, but are caused by systemic abnormalities such as metabolic syndrome, in which the combination of high blood pressure, high cholesterol, and diabetes causes confusion and memory loss.</p>
