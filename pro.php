					<h3> Members of the Society fall into four categories. These are full members, associate
members, affiliate members and honorary members.</h3>
					<h4>Full Membership</h4>
					<p> To be a full member of the society, the applicant must be a pharmacist registered by
the Pharmacists Council of Nigeria (PCN). In addition, s/he is expected to fulfill all
his/her financial obligations to his/her State Branch of the PSN and the National body.
Any person whose name is removed from the register of pharmacists within the
meaning and application of the PCN Decree 91 of 1992 in line with Laws of the Federal
Republic of Nigeria of 2002 and subsequent legislation shall cease to be a member of
the society. Similarly, any person who is declared by a registered psychiatrist as ill and
unfit to perform the functions of a registered pharmacist, shall cease to be a member
of the society.</p>
					<h4>Associate Membership</h4>
					<p> This is currently restricted to Intern Pharmacists.</p>
					<h4>Affiliate Membership</h4>
					<p>Final year pharmacy students of approved institutions for the training of pharmacists
are eligible to become affiliate members of the society.</p>
					<h4>Honorary Membership</h4>
					<p> This is open to non-Nigerians who:</p>
						<blockquote><p>are registered in their countries of domicile as Pharmacists; and</p></blockquote>
						<blockquote><p>by virtue of their contributions, have promoted the course of Pharmacy profession
within and outside Nigeria.</p></blockquote>
