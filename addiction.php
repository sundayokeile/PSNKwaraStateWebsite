                            <h4>What Is Addiction?</h4>
                             <p>Addiction is a complex condition, a brain disease that is manifested by compulsive substance use despite harmful consequence. People with addiction (severe substance use disorder) have an intense focus on using a certain substance(s), such as alcohol or drugs, to the point that it takes over their life. They keep using alcohol or a drug even when they know it will causes problems. Yet a number of effective treatments are available and people can recover from addiction and lead normal, productive lives.</p>
<h4>People can develop an addiction to:</h4>
<p>Alcohol; Marijuana; PCP, LSD and other hallucinogens; Inhalants, such as, paint thinners and glue; Opioid pain killers, such as codeine and oxycodone, heroin; Sedatives, hypnotics and anxiolytics (medicines for anxiety such as tranquilizers); Cocaine, methamphetamine and other stimulants; Tobacco.</p>
<p>People with a substance use disorder have distorted thinking, behavior and body functions. Changes in the brain’s wiring are what cause people to have intense cravings for the drug and make it hard to stop using the drug. Brain imaging studies show changes in the areas of the brain that relate to judgment, decision making, learning, memory and behavior control. These substances can cause harmful changes in how the brain functions. These changes can last long after the immediate effects of the drug — the intoxication. Intoxication is the intense pleasure, calm, increased senses or a high caused by the drug. Intoxication symptoms are different for each substance. Over time people with addiction build up a tolerance, meaning they need larger amounts to feel the effects.</p>
<p>According to research, people begin taking drugs for a variety of reasons, including: to feel good — feeling of pleasure, “high”; to feel better — e.g., relieve stress; to do better — improve performance; curiosity and peer pressure, etc. People with addictive disorders may be aware of their problem, but be unable to stop it even if they want to. The addiction may cause health problems as well as problems at work and with family members and friends. The misuse of drugs and alcohol is the leading cause of preventable illnesses and premature death.</p>
<h4>How Is Addiction Treated?</h4>
<p>The first step on the road to recovery is recognition of the problem. The recovery process can be hindered when a person denies having a problem and lacks understanding about substance misuse and addiction. The intervention of concerned friends and family often prompts treatment. Because addiction affects many aspects of a person’s life, multiple types of treatment are often required. For most, a combination of <em><strong>medication and individual or group therapy is most effective</strong></em>. Treatment approaches that address an individual’s situation and any co-occurring medical, psychiatric and social problems can lead to sustained recovery.</p>
<p>Medications are used to control drug cravings and relieve severe symptoms of withdrawal. Therapy can help addicted individuals understand their behavior and motivations, develop higher self-esteem, cope with stress and address other mental health problems.</p>
